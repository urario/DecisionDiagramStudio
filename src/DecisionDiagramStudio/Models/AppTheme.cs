namespace DecisionDiagramStudio.Models;

/// <summary>
/// Identifies the application theme preference stored in session options.
/// </summary>
public enum AppTheme
{
    /// <summary>
    /// Follow the operating system theme.
    /// </summary>
    System,

    /// <summary>
    /// Use the light application theme.
    /// </summary>
    Light,

    /// <summary>
    /// Use the dark application theme.
    /// </summary>
    Dark,
}
